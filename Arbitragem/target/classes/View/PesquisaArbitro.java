    /**
     * @param args the command line arguments
     */
    private void atualizarTabela(List<Arbitro_DAO> listaArbitros) {
        // Criar o modelo da tabela
        DefaultTableModel modeloTabela = (DefaultTableModel) jTable1.getModel();

        // Limpar tabela antes de adicionar novos dados
        modeloTabela.setRowCount(0);

        // Adicionar cada árbitro à tabela
        for (Arbitro_DAO arbitro : listaArbitros) {
            modeloTabela.addRow(new Object[]{arbitro.getNome(), arbitro.getEspecialidade(), arbitro.getRanking()});
        }
    }
    private void carregarTabela() {
        // Obter a lista de árbitros
        List<Arbitro_DAO> listaArbitros = Arbitro_DAO.getArbitros();

        // Atualizar a tabela com todos os árbitros
        atualizarTabela(listaArbitros);
    }
